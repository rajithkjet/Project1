    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            Connection f = DAC.connect();
            
            ResultSet r = f.createStatement().executeQuery("select * from staff where NIC='" + jTextField1.getText() + "'  ");
            
            while (r.next()) {
                String Designation = r.getString("Designation");
                jTextField5.setText(Designation);
                
            }
            ResultSet r1 = f.createStatement().executeQuery("select * from patient where PatientID in ( select PatientID from staff where NIC ='" + jTextField1.getText() + "')");
            
            while (r1.next()) {
                String Initials = r1.getString("Initials");
                String Surname = r1.getString("Surname");
                
                String fname = Initials + " " + Surname;
                jTextField4.setText(fname);
                
                String DOB = r1.getString("DOB");
                jTextField7.setText(DOB);
                
                String Address = r1.getString("Address");
                jTextField9.setText(Address);
                
                String Phone = r1.getString("Phone");
                jTextField6.setText(Phone);
                
                String Gender = r1.getString("Gender");
                jTextField8.setText(Gender);
                
            }
            ResultSet r2 = f.createStatement().executeQuery("select * from patient_allergies where PatientID in ( select PatientID from staff where NIC ='" + jTextField1.getText() + "')");
            Vector v = new Vector();
            
            while (r2.next()) {
                String Allergy = r2.getString("Allergy");
             //   jTextField5.setText(Allergy);
                v.add(Allergy);
            }
            jList1.setListData(v);
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        

    }//GEN-LAST:event_jButton1ActionPerformed

