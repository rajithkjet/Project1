                ResultSet r = std.createStatement().executeQuery("select * from  medirep ");
                int max = 0;
                boolean b = false;
                while (r.next()) {

                    int ID = Integer.parseInt(r.getString("id"));
                    if (max < ID) {
                        max = ID;
                    }
                }

                System.out.println(max);
                String jasp = "Report/MedicalReport.jrxml";
                JasperReport jr = JasperCompileManager.compileReport(jasp);
                Map<String, Object> params = new HashMap<String, Object>();

                params.put("id", max);
