            Connection std = DAC.connect();
            PreparedStatement p = std.prepareStatement("insert into medirep values( '" + id + "','" + jTextField1.getText() + "','" + jTextField2.getText() + "','" + jTextArea1.getText() + "','" + jTextArea2.getText() + "','"+jTextField3.getText()+"')");
