    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            try {
                Connection f=DAC.connect();
                ResultSet r=f.createStatement().executeQuery("select * from medicine where MName='"+jTextField1.getText()+"'  ");
                
                while(r.next()){
                   
                    
                     String brand=r.getString("Brand");
                    jTextField3.setText(brand);
                    
                     String category=r.getString("Category");
                    jTextField4.setText(category);
                    
                     String quentity=r.getString("MQuantity");
                    jTextField6.setText(quentity);
                    
                     String exp=r.getString("ExpDate");
                    jTextField7.setText(exp);
                    
                     String mf=r.getString("MfdDate");
                    jTextField8.setText(mf);
                    
                     String price=r.getString("UnitPrice");
                    jTextField5.setText(price);
                }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

