    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Connection f = DAC.connect();
            String bd = null;
            if (!jTextField1.getText().isEmpty() && !jTextField2.getText().isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Enter only one");
            } 
            else if (!jTextField1.getText().isEmpty()) {
                ResultSet r = f.createStatement().executeQuery("select * from patient where PatientID in (select PatientID from student where Registration_No = '" + jTextField1.getText() + "' )");
                if (r.next()) {

                    String pid = r.getString("PatientID");
                    jTextField4.setText(pid);

                    String fname = r.getString("Initials");
                    jTextField3.setText(fname);

                    String lname = r.getString("Surname");
                    jTextField10.setText(lname);

                    String dob = r.getString("DOB");
                    jTextField7.setText(dob);

                    String address = r.getString("Address");
                    jTextArea1.setText(address);

                    String tp = r.getString("Phone");
                    jTextField8.setText(tp);

                    String gender = r.getString("Gender");
                    jTextField9.setText(gender);

                } else {
                    JOptionPane.showMessageDialog(rootPane, "Student not registred");
                }

            }
            
            ////////////////////////////////////////////////////////////////////
            else if (!jTextField2.getText().isEmpty()) {

                ResultSet r = f.createStatement().executeQuery("select * from patient where PatientID in (select PatientID from staff where NIC = '" + jTextField2.getText() + "' )");
                if (r.next()) {

                    String pid = r.getString("PatientID");
                    jTextField4.setText(pid);

                    String fname = r.getString("Initials");
                    jTextField3.setText(fname);

                    String lname = r.getString("Surname");
                    jTextField10.setText(lname);

                    String dob = r.getString("DOB");
                    jTextField7.setText(dob);

                    String address = r.getString("Address");
                    jTextArea1.setText(address);

                    String tp = r.getString("Phone");
                    jTextField8.setText(tp);

                    String gender = r.getString("Gender");
                    jTextField9.setText(gender);

                } else {
                    JOptionPane.showMessageDialog(rootPane, "Patient not registred");
                }
            }else {
                JOptionPane.showMessageDialog(rootPane, "Enter the NIC or Registration");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_jButton1ActionPerformed

