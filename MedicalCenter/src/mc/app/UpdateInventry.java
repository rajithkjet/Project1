    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            Connection con = DAC.connect();
            Statement stmt = con.createStatement();
            String query1 = "SELECT * FROM inventry WHERE ItemID='" + jTextField1.getText() + "'";

            ResultSet rs = stmt.executeQuery(query1);

            while (rs.next()) {
                jTextField2.setText(rs.getString("ItemName"));
                jTextField3.setText(rs.getString("IStatus"));
                jTextField4.setText(rs.getString("DateRecived"));
                jTextArea1.setText(rs.getString("Description"));

            }
        } catch (Exception e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int n = JOptionPane.showConfirmDialog(null, "Confirm Update", "", JOptionPane.YES_NO_OPTION);
        if (n == JOptionPane.YES_NO_OPTION) {

            try {
                Connection con = DAC.connect();
                PreparedStatement pst;
                String sql = "UPDATE inventry SET  IStatus='" + jTextField3.getText() + "' , DateRecived='" + jTextField4.getText() + "' , Description='" + jTextArea1.getText() + "' WHERE ItemID='" + jTextField1.getText() + "' ";
                pst = con.prepareStatement(sql);
                pst.executeUpdate();

            } catch (Exception e) {
                e.printStackTrace();
            }
            JOptionPane.showMessageDialog(this, "Successfully updated");
                   
        } else {

            JOptionPane.showMessageDialog(null, "Cancelled update");
            
        }
    }//GEN-LAST:event_jButton3ActionPerformed

