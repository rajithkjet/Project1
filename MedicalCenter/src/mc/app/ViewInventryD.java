    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         try {
            Connection f = DAC.connect();
            
            ResultSet r = f.createStatement().executeQuery("select * from Inventry where ItemID='" + jTextField1.getText() + "'  ");
           
            while (r.next()) {
            
               
                String DateRecived = r.getString("DateRecived");
                jTextField3.setText(DateRecived);
                 String IStatus = r.getString("IStatus");
                jTextField4.setText(IStatus);
                  String ItemName = r.getString("ItemName");
                jTextField2.setText(ItemName);
                
                String Description = r.getString("Description");
                jTextArea1.setText(Description);
                
                
            }
           
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

