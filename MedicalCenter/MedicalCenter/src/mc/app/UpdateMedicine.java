    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
             try {
                Connection f=DAC.connect();
                ResultSet r=f.createStatement().executeQuery("select * from medicine where MName='"+jTextField1.getText()+"'  ");
                
                while(r.next()){
                    
                    
                     String brand=r.getString("Brand");
                    jTextField2.setText(brand);
                    
                     String category=r.getString("Category");
                    jTextField7.setText(category);
                    
                     String quentity=r.getString("MQuantity");
                    jTextField8.setText(quentity);
                    
                     String exp=r.getString("ExpDate");
                    jTextField6.setText(exp);
                    
                     String mf=r.getString("MfdDate");
                    jTextField5.setText(mf);
                    
                     String price=r.getString("UnitPrice");
                    jTextField4.setText(price);
                }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
             
                    
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       int n = JOptionPane.showConfirmDialog(  
                null,
                "confirm update" ,
                "",
                JOptionPane.YES_NO_OPTION);

      if(n == JOptionPane.YES_OPTION)
      {
            try {
              Connection a=DAC.connect();
              PreparedStatement p=a.prepareStatement("Update medicine set Mname='"+jTextField1.getText()+"',Brand='"+jTextField2.getText()+"',Category='"+jTextField7.getText()+"',MQuantity='"+jTextField8.getText()+"',ExpDate='"+jTextField6.getText()+"',MfdDate='"+jTextField5.getText()+"',UnitPrice='"+jTextField4.getText()+"' where Mname='"+jTextField1.getText()+"'  ");
              p.executeUpdate();
              p.close();
              
            
              
        } catch (Exception e) {
            e.printStackTrace();
        }
             JOptionPane.showMessageDialog(this, "Succesfully updated!");
    }                                        

      
      else
      {
          JOptionPane.showMessageDialog(null, "update cancelled!");
          System.exit(0);}
          
        
         
    }//GEN-LAST:event_jButton3ActionPerformed

